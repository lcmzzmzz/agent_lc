"""
【正经注释】
草稿状态类型定义模块。
使用 TypedDict 定义 DraftState 类型字典，用于在多代理协作流程中
传递和管理草稿的编写、审核和修订状态信息。

【大白话注释】
这个文件定义了一个叫 DraftState 的"数据表格模板"。
它用来存草稿的各种信息：任务是什么、主题是啥、草稿内容、审核意见、修改笔记。
就像一张表格，每个格子都有固定的名字，往里面填数据就行。
"""

from typing import TypedDict, List, Annotated  # 正经注释：类型提示相关模块，TypedDict 用于定义带类型约束的字典 / 大白话注释：导入类型工具，用来定义"这个字典里该有哪些字段"
import operator  # 正经注释：运算符模块，在 Annotated 类型中用于指定归约操作 / 大白话注释：导入操作符，配合类型标注使用


class DraftState(TypedDict):
    """草稿状态类型字典，用于在草稿编写流程中传递状态信息。

    【正经注释】
    定义草稿生命周期中的关键状态字段，包括任务定义、主题、草稿内容、
    审核反馈和修订笔记，支持多代理协作时的状态传递。

    【大白话注释】
    这个"表格模板"有以下几列：
    - task: 任务描述（比如"写一篇关于AI的报告"）
    - topic: 主题关键词
    - draft: 草稿的具体内容
    - review: 审核人给出的反馈意见
    - revision_notes: 修改时的备注说明
    """

    task: dict  # 正经注释：任务定义字典，包含任务相关的配置和参数 / 大白话注释：任务详情，用字典存着
    topic: str  # 正经注释：草稿主题字符串 / 大白话注释：这篇草稿的主题是什么
    draft: dict  # 正经注释：草稿内容字典，包含草稿正文及相关元信息 / 大白话注释：草稿内容，用字典存着
    review: str  # 正经注释：审核反馈文本 / 大白话注释：审核人看完后说了啥
    revision_notes: str  # 正经注释：修订备注文本 / 大白话注释：修改的时候记了什么笔记
